import streamlit as st
import pandas as pd
from sklearn.linear_model import LogisticRegression
from sklearn.model_selection import train_test_split

# -----------------------------------
# Page Configuration
# -----------------------------------
st.set_page_config(
    page_title="Insurance Policy Prediction",
    page_icon="🏠",
    layout="centered"
)

st.title("🏠Insurance Policy Prediction")
st.write("Predict Insurance Policy using Logistic Regression")

# -----------------------------------
# Load Dataset
# -----------------------------------
df = pd.read_csv("insurance_data.csv")

st.subheader("Dataset")
st.dataframe(df)

# -----------------------------------
# Train Model
# -----------------------------------
X = df[['age']]
y = df['bought_insurance']
X_train, X_test, y_train, y_test = train_test_split(
    X, y, train_size=0.8, random_state=42)

model = LogisticRegression()
model.fit(X_train, y_test)

# -----------------------------------
# User Input
# -----------------------------------
st.subheader("Enter Person Name")

age = st.number_input(
    "Age (In years )",
    min_value=10,
    max_value=65,
    value=18,
    step=1
)

# -----------------------------------
# Prediction
# -----------------------------------
if st.button("Predict Insurance"):

    prediction = model.predict([[age]])

    if prediction[0] == 1:
        st.success("The person is likely to buy insurance.")
    else:
        st.warning("The person is not likely to buy insurance.")

# -----------------------------------
# Model Information
# -----------------------------------
st.subheader("Model Details")
st.write("Coefficient:", model.coef_[0][0])
st.write("Intercept:", model.intercept_[0])
